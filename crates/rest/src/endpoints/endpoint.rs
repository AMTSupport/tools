use anyhow::Result;
use clap::Parser;

pub trait Endpoint {
    type Args: Parser;
    type Request;
    type Response;

    fn new(args: Self::Args) -> Self;

    async fn handle(&self, event: Self::Request) -> Result<Self::Response>;
}
